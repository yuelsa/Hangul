package com.example.hangul.model


val BASIC_CONSONANTS = listOf("ㄱ","ㄴ","ㄷ","ㄹ","ㅁ","ㅂ","ㅅ","ㅇ","ㅈ","ㅊ","ㅋ","ㅌ","ㅍ","ㅎ")
val BASIC_VOWELS = listOf("ㅏ","ㅑ","ㅓ","ㅕ","ㅗ","ㅛ","ㅜ","ㅠ","ㅡ","ㅣ")


val CHOSEONG = listOf("ㄱ","ㄲ","ㄴ","ㄷ","ㄸ","ㄹ","ㅁ","ㅂ","ㅃ","ㅅ","ㅆ","ㅇ","ㅈ","ㅉ","ㅊ","ㅋ","ㅌ","ㅍ","ㅎ")
val JUNGSEONG = listOf("ㅏ","ㅐ","ㅑ","ㅒ","ㅓ","ㅔ","ㅕ","ㅖ","ㅗ","ㅘ","ㅙ","ㅚ","ㅛ","ㅜ","ㅝ","ㅞ","ㅟ","ㅠ","ㅡ","ㅢ","ㅣ")
val JONGSEONG = listOf("","ㄱ","ㄲ","ㄳ","ㄴ","ㄵ","ㄶ","ㄷ","ㄹ","ㄺ","ㄻ","ㄼ","ㄽ","ㄾ","ㄿ","ㅀ","ㅁ","ㅂ","ㅄ","ㅅ","ㅆ","ㅇ","ㅈ","ㅊ","ㅋ","ㅌ","ㅍ","ㅎ")


val BASIC_FINALS = listOf(
    "", "ㄱ","ㄴ","ㄷ","ㄹ","ㅁ","ㅂ","ㅅ","ㅇ","ㅈ","ㅊ","ㅋ","ㅌ","ㅍ","ㅎ"
)

fun composeSyllable(choseong: String, jungseong: String, jongseong: String = ""): String {
    val ci = CHOSEONG.indexOf(choseong)
    val vi = JUNGSEONG.indexOf(jungseong)
    val ti = JONGSEONG.indexOf(jongseong)
    require(ci >= 0 && vi >= 0 && ti >= 0) { "Invalid jamo: $choseong $jungseong $jongseong" }
    val code = 0xAC00 + (ci * 21 + vi) * 28 + ti
    return String(intArrayOf(code), 0, 1)
}